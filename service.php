    <script>
        // Автоматическая прокрутка
        const gallery = document.getElementById('autoScrollGallery');
        let scrollInterval;
        
        function startAutoScroll() {
            scrollInterval = setInterval(() => {
                if (gallery.scrollLeft + gallery.offsetWidth >= gallery.scrollWidth - 10) {
                    gallery.scrollTo({ left: 0, behavior: 'smooth' });
                } else {
                    gallery.scrollBy({ left: 300, behavior: 'smooth' });
                }
            }, 3000);
        }
        
        function stopAutoScroll() {
            clearInterval(scrollInterval);
        }
        
        // Ручное управление
        function scrollGallery(direction) {
            stopAutoScroll();
            gallery.scrollBy({ left: direction * 300, behavior: 'smooth' });
            setTimeout(startAutoScroll, 5000);
        }
        
        // Инициализация
        document.addEventListener('DOMContentLoaded', () => {
            startAutoScroll();
            
            // Пауза при наведении
            gallery.addEventListener('mouseenter', stopAutoScroll);
            gallery.addEventListener('mouseleave', startAutoScroll);
            
            // Для мобильных устройств
            gallery.addEventListener('touchstart', stopAutoScroll);
            gallery.addEventListener('touchend', () => setTimeout(startAutoScroll, 5000));
        });  
        </script>
